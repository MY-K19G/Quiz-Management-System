package k19g.quiz.entity;

/**
 * Enum representing the difficulty levels for quiz questions.
 * Each level corresponds to a different degree of challenge for the quiz takers.
 * 
 * Author: K19G
 */
public enum Level {
    EASY,    // Represents easy difficulty level
    MEDIUM,  // Represents medium difficulty level
    HARD,    // Represents hard difficulty level
    EXPERT   // Represents expert difficulty level
}
